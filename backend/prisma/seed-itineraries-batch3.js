async function main() {
  console.log('🌏 Seeding Batch 3 — 5-day itineraries...\n')
  let created = 0
  let skipped = 0

  for (const { dayPlans, ...data } of ITINERARIES) {
    const exists = await prisma.itinerary.findUnique({ where: { slug: data.slug } })
    if (exists) {
      console.log(`  ⏭  Skipped (already exists): ${data.slug}`)
      skipped++
      continue
    }

    await prisma.itinerary.create({
      data: {
        ...data,
        publishedAt: data.published ? new Date() : null,
        dayPlans: {
          create: dayPlans.map((dp, i) => ({ ...dp, order: i })),
        },
      },
    })
    console.log(`  ✓  Created: ${data.title}`)
    created++
  }

  console.log(`\n✅ Batch 3 done — ${created} created, ${skipped} skipped`)
}

main()
  .catch((e) => { console.error(e); process.exit(1) })
  .finally(() => prisma.$disconnect())
